function ensureSession() {
  if (state.session.length) return;
  let questions = allQuestions().filter((item) => item.subject === state.practiceSubject);
  if (state.practiceMode === "verified500") {
    questions = questions.filter((item) => item.bank === "verified500");
  }
  if (state.chapter !== "全部") {
    questions = questions.filter((item) => item.chapter === state.chapter);
  }
  if (state.practiceMode === "wrong") {
    questions = questions.filter((item) => store.wrong[item.id]);
  }
  if (state.query) {
    const query = state.query.toLowerCase();
    questions = questions.filter((item) => [item.question, item.explanation, item.memory, item.chapter, ...item.options].join(" ").toLowerCase().includes(query));
  }
  if (state.practiceMode === "random" || state.practiceMode === "exam" || state.practiceMode === "wrong") {
    questions = shuffle(questions);
  }
  if (state.practiceMode === "exam") {
    if (state.practiceSubject === "科目一") {
      questions = questions.filter((item) => item.type === "single" || item.type === "judge");
    }
    const limit = examLimit(state.practiceSubject);
    questions = questions.slice(0, limit);
  }
  state.session = questions;
  if (state.practiceMode === "exam") {
    state.exam = {
      subject: state.practiceSubject,
      startedAt: Date.now(),
      answers: {},
      finished: false,
      result: null
    };
  }
}

function getActive(questionId) {
  if (state.practiceMode === "exam" && state.exam) {
    if (!state.exam.answers[questionId]) {
      state.exam.answers[questionId] = { id: questionId, selected: [], checked: false, correct: false, revealed: false };
    }
    state.active = state.exam.answers[questionId];
    return state.active;
  }
  if (!state.active || state.active.id !== questionId) {
    state.active = { id: questionId, selected: [], checked: false, correct: false, revealed: false };
  }
  return state.active;
}

function selectAnswer(index) {
  const question = state.session[state.current];
  if (!question) return;
  const active = getActive(question.id);
  if (active.checked) return;
  if (question.type === "multi") {
    active.selected = active.selected.includes(index) ? active.selected.filter((item) => item !== index) : [...active.selected, index];
    render();
    return;
  }
  active.selected = [index];
  if (state.practiceMode === "exam") {
    render();
    return;
  }
  submitAnswer(false);
}

function submitAnswer(revealed) {
  const question = state.session[state.current];
  if (!question) return;
  const active = getActive(question.id);
  if (state.practiceMode === "exam") return;
  if (!active.selected.length && !revealed) return;
  const correct = sameSet(active.selected, question.answer);
  active.checked = true;
  active.correct = correct;
  active.revealed = revealed;
  if (!revealed) {
    recordAnswer(question.id, correct);
  }
  render();
}

function recordAnswer(id, correct) {
  store.answered += 1;
  if (correct) {
    store.correct += 1;
  } else {
    store.wrong[id] = (store.wrong[id] || 0) + 1;
  }
  store.history.unshift({ id, correct, at: new Date().toISOString() });
  store.history = store.history.slice(0, 200);
  const previous = store.questionStats?.[id] || { attempts: 0, correct: 0, lastCorrect: false };
  store.questionStats = {
    ...(store.questionStats || {}),
    [id]: {
      attempts: previous.attempts + 1,
      correct: previous.correct + (correct ? 1 : 0),
      lastCorrect: correct,
      updatedAt: new Date().toISOString()
    }
  };
  saveStore();
}

function nextQuestion() {
  if (!state.session.length) return;
  state.current = Math.min(state.current + 1, state.session.length - 1);
  state.active = null;
  render();
}

function prevQuestion() {
  state.current = Math.max(state.current - 1, 0);
  state.active = null;
  render();
}

