document.addEventListener("click", handleClick);
document.addEventListener("change", handleChange);
function handleSearchInput(event) {
  state.query = event.target.value.trim();
  searchInput.value = state.query;
  if (mobileSearchInput) mobileSearchInput.value = state.query;
  resetSession();
  render();
}

searchInput.addEventListener("input", handleSearchInput);
mobileSearchInput?.addEventListener("input", handleSearchInput);
importInput.addEventListener("change", importQuestions);
progressImportInput.addEventListener("change", importProgress);

render();

function handleClick(event) {
  const target = event.target.closest("[data-action]");
  if (!target) return;
  const action = target.dataset.action;

  if (action === "view") setView(target.dataset.view);
  if (action === "subject") openSubject(target.dataset.subject);
  if (action === "toggle-mobile-menu") toggleMobileMenu();
  if (action === "set-subject") setPracticeSubject(target.dataset.subject);
  if (action === "set-mode") setPracticeMode(target.dataset.mode);
  if (action === "set-chapter") setChapter(target.dataset.chapter);
  if (action === "answer") selectAnswer(Number(target.dataset.index));
  if (action === "submit") submitAnswer(false);
  if (action === "reveal") submitAnswer(true);
  if (action === "next") nextQuestion();
  if (action === "prev") prevQuestion();
  if (action === "favorite") toggleFavorite(target.dataset.id);
  if (action === "mastered") markMastered(target.dataset.id);
  if (action === "practice-id") jumpToQuestion(target.dataset.id);
  if (action === "clear-wrong") clearWrong(target.dataset.id);
  if (action === "export-progress") exportProgress();
  if (action === "import-progress") progressImportInput.click();
  if (action === "export-bank") exportBank();
  if (action === "import-bank") importInput.click();
  if (action === "reset-progress") resetProgress();
  if (action === "finish-exam") finishExam();
  if (action === "restart-exam") restartExam();
  if (action === "review-exam-wrong") reviewExamWrong();
  if (action === "start-verified-500") startVerified500();
  if (action === "copy-enrollment-questions") copyEnrollmentQuestions();
}

function handleChange(event) {
  const target = event.target;
  if (target.id === "chapterSelect") {
    setChapter(target.value);
  }
  if (target.matches("[data-guide-check]")) {
    toggleGuideCheck(target.dataset.guideCheck, target.checked);
  }
}

function setView(view) {
  state.view = view;
  state.mobileMenuOpen = false;
  render();
  scrollPageTop();
}

function openSubject(subject) {
  state.view = subject === "科目一" ? "subject1" : subject === "科目二" ? "subject2" : subject === "科目三" ? "subject3" : "subject4";
  state.practiceSubject = subject;
  state.mobileMenuOpen = false;
  resetSession();
  render();
  scrollPageTop();
}

function toggleMobileMenu() {
  state.mobileMenuOpen = !state.mobileMenuOpen;
  render();
}

function setPracticeSubject(subject) {
  state.view = subjectView(subject);
  state.practiceSubject = subject;
  if (subject !== "科目一" && state.practiceMode === "verified500") {
    state.practiceMode = "random";
  }
  state.chapter = "全部";
  state.mobileMenuOpen = false;
  resetSession();
  render();
}

function setPracticeMode(mode) {
  if (mode === "verified500") {
    state.view = "subject1";
    state.practiceSubject = "科目一";
  }
  state.practiceMode = mode;
  if (mode === "exam") {
    state.chapter = "全部";
    state.query = "";
    searchInput.value = "";
    if (mobileSearchInput) mobileSearchInput.value = "";
  }
  resetSession();
  render();
}

function setChapter(chapter) {
  state.chapter = chapter;
  resetSession();
  render();
}

function resetSession() {
  state.session = [];
  state.current = 0;
  state.active = null;
  state.exam = null;
}

